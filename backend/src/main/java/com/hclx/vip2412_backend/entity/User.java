package com.hclx.vip2412_backend.entity;

import lombok.Data;
import lombok.ToString;

import java.util.Date;

/**
 * 用户实体类
 */
@Data
@ToString
public class User {

    /**
     * 用户ID
     */
    private Long id;

    /**
     * 用户名
     */
    private String username;

    /**
     * 密码
     */
    private String password;

    /**
     * 角色
     */
    private String role;

    /**
     * 邮箱
     */
    private String email;

    /**
     * 创建时间
     */
    private Date createTime;

    /**
     * 更新时间
     */
    private Date updateTime;

}
